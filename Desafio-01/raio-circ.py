# Construir um algoritmo para ler o raio de uma circunferência e
# mostrar o perímetro ( 2 x π x raio ) e a área ( π x raio2 ). Utilize o π como constante.

raio = float(input("Digite o valor do raio: "))
perimetro = 2 * 3.14 * raio
area = 3.14 * raio ** 2
print("O perímetro da circunferência é: ", perimetro)
print("A área da circunferência é: ", area)
